#include "libft.h"

void	ft_strclr(char *s)
{
	int i;
	int len;

	if (!s)
		return ;
	len = ft_strlen(s);
	i = 0;
	while (i != len + 1)
	{
		s[i] = '\0';
		i++;
	}
}
